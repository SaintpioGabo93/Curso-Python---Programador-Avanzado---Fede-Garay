x = 6
y = 2
z = 7
k = 16
 # Suma
print(f'{x} mas {y} es igual a: {x+y}')
 # Resta
print(f'{x} menos {y} es igual a: {x-y}')
 # Multiplicación
print(f'{x} por {y} es igual a: {x*y}')
 # División
print(f'{x} entre {y} es igual a: {x/y}')
 # División al piso es truncar un valor decimal
print(f'{z} dividido al piso {y} es igual a: {z//y}')
 # Módulo para matemáticas discretas
print(f'{z} módulo {y} es: {z%y}')
 # Potencia
print(f'{x} a la  {z} potencia es igual a: {x**z}')
 # Raíz cuadrada
print(f'La raíz cuadrada de {k} es: {k**0.5}')